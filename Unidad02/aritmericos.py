a = 15
b = 24

#suma
suma = a + b
print("El resultado de la suma es ",suma)

#resta
resta = a-b
print("El resultado de la resta es ",resta)

#multiplicación
multi = a*b
print("El resultado de la multiplicación es ",multi)

#División
div = a/b
print("El resultado de la división es ",div)

#Division entera
div = a//b
print("El resultado de la división entera es ",div)

#Modulo
mod = a%b
print("El resultado del modulo es ",mod)

#Exponentes
exp = a**b
print("El resultado de la potencia es: ",exp)